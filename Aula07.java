
package basico;

// Variaveis do Tipo Constante

public class Aula07 {

	
	public static void main(String[] args) {
		
		int num = 20;
		
		System.out.println("O valor é: " + num);
		
		num = 5;
		
		System.out.println("O valor é: " + num);
		
		// Variável do tipo constante:
		
		final int valor = 20;
		
		System.out.println(valor);
		
		
		
		

	}

}
